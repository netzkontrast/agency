def test_export_captures_edges(engine, iid, tmp_path):
    _call(engine, iid, "note",
          observation="x", plan_slug="020")
    target = tmp_path / "x.json"
    r = _call(engine, iid, "export", path=str(target))
    assert r["edges"] >= 1
    data = json.loads(target.read_text())
    edges = data["edges"]
    edge_types = {e.get("type") for e in edges}
    # dogfood.note links OBSERVED_DURING + SERVES.
    assert "OBSERVED_DURING" in edge_types or "SERVES" in edge_types


