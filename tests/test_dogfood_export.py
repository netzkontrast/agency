"""Spec 020 finish — dogfood.export JSON dump.

The merge-conflict recovery fallback (Spec 020 line 69-73): dump the
bi-temporal graph to a portable JSON file so a conflicted DB can be
regenerated by replaying both branches' JSON exports.

Tests:
  - export of empty graph produces well-formed JSON shape
  - export captures Reflection nodes + properties
  - export captures Intent nodes with the Spec 048 owner + parent_intent_id
  - export captures graph edges (SERVES, OBSERVED_DURING, PARENT_INTENT)
  - export writes to the given path; returns {path, nodes, edges, bytes}
  - export defaults to .agency/export-<ts>.json when no path given
  - export is pure (transform) — no graph writes
"""
import json
import os
import tempfile

import pytest

from agency.engine import Engine


@pytest.fixture
def engine():
    return Engine(tempfile.mktemp(suffix=".db"))


@pytest.fixture
def iid(engine):
    return engine.intent.capture_and_confirm(
        "test dogfood.export", "json dump", "x", owner="user")


def _call(engine, iid, verb, **kw):
    r, _ = engine.registry.invoke(
        engine.memory, iid, "dogfood", verb,
        agent_id="agent:test", **kw)
    return r


# ---------------------------------------------------------------------------
# Verb registration + payload shape.
# ---------------------------------------------------------------------------


def test_export_verb_registered(engine):
    cap = engine.registry.get("dogfood")
    assert "export" in cap.verbs


def test_export_returns_payload_with_path_and_counts(engine, iid, tmp_path):
    target = tmp_path / "export.json"
    r = _call(engine, iid, "export", path=str(target))
    assert "path" in r and r["path"] == str(target)
    assert "nodes" in r and isinstance(r["nodes"], int)
    assert "edges" in r and isinstance(r["edges"], int)
    assert "bytes" in r and isinstance(r["bytes"], int)


def test_export_writes_well_formed_json(engine, iid, tmp_path):
    target = tmp_path / "x.json"
    _call(engine, iid, "export", path=str(target))
    assert target.exists()
    data = json.loads(target.read_text())
    # Top-level keys per Spec 020 §"export shape" (panel addition).
    assert "version" in data
    assert "nodes" in data and isinstance(data["nodes"], list)
    assert "edges" in data and isinstance(data["edges"], list)


# ---------------------------------------------------------------------------
# Node capture: Reflections + Intents.
# ---------------------------------------------------------------------------


def test_export_captures_reflection_nodes(engine, iid, tmp_path):
    _call(engine, iid, "note",
          observation="export test obs", plan_slug="020-central-graph-db")
    target = tmp_path / "x.json"
    r = _call(engine, iid, "export", path=str(target))
    assert r["nodes"] >= 2   # at least the Intent + the Reflection
    data = json.loads(target.read_text())
    reflections = [n for n in data["nodes"] if n.get("label") == "Reflection"]
    assert reflections
    me = reflections[0]
    assert "id" in me and "properties" in me
    assert me["properties"].get("text") == "export test obs"
    assert me["properties"].get("plan_slug") == "020-central-graph-db"


def test_export_captures_intent_with_owner_and_parent(engine, iid, tmp_path):
    """Spec 048 extension: Intent's owner + parent_intent_id round-trip
    through the JSON export."""
    child = engine.intent.capture_and_confirm(
        "child", "x", "x", parent_intent_id=iid)
    target = tmp_path / "x.json"
    _call(engine, iid, "export", path=str(target))
    data = json.loads(target.read_text())
    intents = [n for n in data["nodes"] if n.get("label") == "Intent"]
    parent = next(i for i in intents if i["id"] == iid)
    me = next(i for i in intents if i["id"] == child)
    assert parent["properties"]["owner"] == "user"
    assert me["properties"]["owner"] == "agent"
    assert me["properties"]["parent_intent_id"] == iid


