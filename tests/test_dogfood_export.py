def test_export_default_path_under_dot_agency(engine, iid, tmp_path,
                                                monkeypatch):
    """When path is empty, export defaults to .agency/export-<ts>.json
    under the current working directory."""
    monkeypatch.chdir(tmp_path)
    agency_dir = tmp_path / ".agency"
    agency_dir.mkdir()
    r = _call(engine, iid, "export", path="")
    assert r["path"].startswith(str(agency_dir))
    assert r["path"].endswith(".json")
    assert os.path.isfile(r["path"])


def test_export_is_pure_no_graph_writes(engine, iid, tmp_path):
    """Export is a transform — calling it must not change the node count."""
    _call(engine, iid, "note", observation="x", plan_slug="x")
    before_nodes = len(list(engine.memory.find("Reflection")))
    before_intents = len(list(engine.memory.find("Intent")))
    _call(engine, iid, "export", path=str(tmp_path / "x.json"))
    after_nodes = len(list(engine.memory.find("Reflection")))
    after_intents = len(list(engine.memory.find("Intent")))
    assert before_nodes == after_nodes
    assert before_intents == after_intents


def test_export_default_path_uses_ns_to_avoid_collision(engine, iid, tmp_path,
                                                         monkeypatch):
    """sc:sc-analyze F6 review: two exports in quick succession must
    not clobber each other (review found time.time() would collide;
    fix uses time.time_ns)."""
    monkeypatch.chdir(tmp_path)
    (tmp_path / ".agency").mkdir()
    r1 = _call(engine, iid, "export", path="")
    r2 = _call(engine, iid, "export", path="")
    assert r1["path"] != r2["path"], \
        "default paths must differ even when called back-to-back"
    assert os.path.isfile(r1["path"])
    assert os.path.isfile(r2["path"])


def test_export_empty_graph_returns_valid_json(tmp_path):
    """An engine with only the bootstrap Intent should export to valid
    JSON with at least version + nodes + edges keys (empty lists ok)."""
    e = Engine(tempfile.mktemp(suffix=".db"))
    i = e.intent.capture_and_confirm("empty", "x", "x", owner="user")
    r, _ = e.registry.invoke(
        e.memory, i, "dogfood", "export",
        agent_id="agent:test", path=str(tmp_path / "empty.json"))
    data = json.loads((tmp_path / "empty.json").read_text())
    assert data["version"] >= 1
    assert isinstance(data["nodes"], list)
    assert isinstance(data["edges"], list)
    # Should contain the bootstrap Intent at minimum.
    intents = [n for n in data["nodes"] if n.get("label") == "Intent"]
    assert any(n["id"] == i for n in intents)
