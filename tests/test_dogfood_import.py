def test_import_preserves_node_ids(engine, iid, tmp_path):
    _call(engine, iid, "note",
          observation="round-trip text", plan_slug="020-rt")
    target = tmp_path / "x.json"
    _call(engine, iid, "export", path=str(target))
    src_payload = json.loads(target.read_text())
    src_ids = {n["id"] for n in src_payload["nodes"]}

    fresh = Engine(tempfile.mktemp(suffix=".db"))
    fresh_iid = fresh.intent.capture_and_confirm(
        "fresh", "x", "x", owner="user")
    fresh.registry.invoke(
        fresh.memory, fresh_iid, "dogfood", "import",
        agent_id="agent:test", path=str(target))

    after_rows = fresh.memory.g.query("MATCH (n) RETURN n")
    after_ids = {r["n"].get("properties", {}).get("id") for r in after_rows}
    # every original id appears in the fresh DB
    missing = src_ids - after_ids
    assert not missing, f"missing imported ids: {missing}"


