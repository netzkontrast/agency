"""Spec 020 v2 — dogfood.import JSON replay.

The matching reverse of dogfood.export (Spec 020 line 69-73): read a
portable JSON file and replay it into a (typically fresh) DB so a
conflicted DB can be regenerated by replaying both branches' JSON
exports.

Round-trip discipline: a graph exported, wiped, and re-imported must
contain every original node (same id, same properties, same vfrom/vto
window) and every original edge. Closes the merge-conflict recovery
loop.
"""
import json
import tempfile

import pytest

from agency.engine import Engine


@pytest.fixture
def engine():
    return Engine(tempfile.mktemp(suffix=".db"))


@pytest.fixture
def iid(engine):
    return engine.intent.capture_and_confirm(
        "test dogfood.import", "json replay", "x", owner="user")


def _call(engine, iid, verb, **kw):
    r, _ = engine.registry.invoke(
        engine.memory, iid, "dogfood", verb,
        agent_id="agent:test", **kw)
    return r


# ---------------------------------------------------------------------------
# Verb registration + payload shape.
# ---------------------------------------------------------------------------


def test_import_verb_registered(engine):
    cap = engine.registry.get("dogfood")
    assert "import" in cap.verbs


def test_import_returns_counts_and_version(engine, iid, tmp_path):
    target = tmp_path / "x.json"
    _call(engine, iid, "export", path=str(target))
    fresh = Engine(tempfile.mktemp(suffix=".db"))
    fresh_iid = fresh.intent.capture_and_confirm("r", "x", "x", owner="user")
    r, _ = fresh.registry.invoke(
        fresh.memory, fresh_iid, "dogfood", "import",
        agent_id="agent:test", path=str(target))
    assert "imported_nodes" in r and isinstance(r["imported_nodes"], int)
    assert "imported_edges" in r and isinstance(r["imported_edges"], int)
    assert r["version"] == 1
    assert r["imported_nodes"] >= 1   # the source's bootstrap intent


def test_import_rejects_unknown_version(engine, iid, tmp_path):
    target = tmp_path / "x.json"
    payload = {"version": 999, "generated_at": 0, "nodes": [], "edges": []}
    target.write_text(json.dumps(payload))
    with pytest.raises(ValueError, match="version"):
        _call(engine, iid, "import", path=str(target))


def test_import_missing_file_raises(engine, iid, tmp_path):
    target = tmp_path / "does-not-exist.json"
    with pytest.raises(FileNotFoundError):
        _call(engine, iid, "import", path=str(target))


