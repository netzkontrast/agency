"""Setup for the Agency Plugin for Claude Code.

This is the "in setup" that maps the harness-in-harness MICRO-skills (the engine's
capability verbs) into MACRO-skills (the capabilities themselves) behind a single
`help` discovery surface. It is SELF-HOSTED: the manifest, the MCP-server
declaration, the marketplace catalogue, the `help` skill, and the `/agency:help`
command are all generated by DOGFOODING the plugin-development capability
(`plugin.scaffold` / `author_skill` / `author_command`) over the live registry —
the engine authors its own Claude Code plugin.

Usage::

    python -m agency.install                       # regen manifest etc into the
                                                   # current install root
    python -m agency.install --scaffold-db         # also create .agency/ + the
                                                   # .gitattributes binary marker
                                                   # (Spec 020); idempotent
    python -m agency.install <root>                # write under <root> instead
                                                   # of the package's repo root

The bash wrapper ``bin/agency-install`` calls ``python -m agency.install
--scaffold-db`` on fresh installs so the Spec 020 ``.agency/`` directory
lands in $1 (= CLAUDE_PLUGIN_ROOT under marketplace install; the local
project root during dev).
"""
